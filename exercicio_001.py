'''
Construa um programa que leia uma temperatura em Fahrenheit e a converta para Celsius. Sabe-se que: °C = (°F - 32) / 1.8.
'''

temperaturaF = int(input("Quantos graus está fazendo agora? "))
temperaturaC = (temperaturaF -32)/1.8

print("Sua temperatra em Fahrenheit é: ", temperaturaF)
print("Sua temperatura em graus Celsius é: ", temperaturaC)